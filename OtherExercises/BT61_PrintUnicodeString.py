"""
In chuỗi Unicode "Hello world".

Gợi ý: Sử dụng định dạng u'string' 
để định nghĩa chuỗi Unicode.
"""

if __name__ == "__main__":
    while True:
        try:
            s = u'Hello world'
            break
        except:
            print("\nTry Again!")

    print(s)
    
    
    
    

    
        
    